#include<stdio.h>

int main()
{
	int arr[10] = {1,3,3,4,5,7,8,3,9,3};
	for (int i = 0; i < 10; i++)
	{
		if (arr[i] == 3)//判断一下是否有3的元素出现如果出现就执行下面的循环把3往前给替换掉;注意一下
		{
			for (int j = i; j < 10; j++)//需要改变的下标跟着唯一的变量同步，使其
			{
				arr[j] = arr[j + 1];//把后面的元素重新覆盖，排除掉3
			}
			i--;//重新回到之前的下标进行检查，确认3已经全部覆盖
		}
	}
	for (int i = 0; i < 6;i++)
	{
		printf("%d    ",arr[i]); //逐一打印剩下的元素，一定要合理不要过大，否者会出现乱码的情况
	}
	getchar();
	getchar();
	return 0;
}